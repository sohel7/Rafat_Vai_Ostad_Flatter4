// const is a keyword used to declare a compile-time constant value. 
//  Constants are values that are known at compile time and cannot be changed at runtime.

void main() {
  const int numberOfSchool = 23;
  final String name;
  name = "rahim";
  print(numberOfSchool);
  print(name);

  var username;
  username = "tanmoy";
  username = 'user';

  dynamic age = 34;
  age = 35;
  age = 'Rafat';
  age = true;
}
